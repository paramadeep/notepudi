.score-line {
  margin-top: 20px;
  display: flex;
  flex-direction: row;
  justify-content: space-around;
}

.score-value {
  font-size: 15pt;
  color: #2c3e50;
}

.score-label {
  font-size: 8pt;
  color: grey;
}
</style>
